package org.mentalizr.scheduler;

public class M7rSchedulerInitializationException extends M7rSchedulerException {

    public M7rSchedulerInitializationException() {
    }

    public M7rSchedulerInitializationException(String message) {
        super(message);
    }

    public M7rSchedulerInitializationException(String message, Throwable cause) {
        super(message, cause);
    }

    public M7rSchedulerInitializationException(Throwable cause) {
        super(cause);
    }

    public M7rSchedulerInitializationException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
