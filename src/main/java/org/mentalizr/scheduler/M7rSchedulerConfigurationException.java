package org.mentalizr.scheduler;

@SuppressWarnings("unused")
public class M7rSchedulerConfigurationException extends M7rSchedulerInitializationException {

    public M7rSchedulerConfigurationException() {
    }

    public M7rSchedulerConfigurationException(String message) {
        super(message);
    }

    public M7rSchedulerConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }

    public M7rSchedulerConfigurationException(Throwable cause) {
        super(cause);
    }

    public M7rSchedulerConfigurationException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
